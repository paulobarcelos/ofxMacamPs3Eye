void MiniDrawSpace(unsigned char* pixMap, short pixBytes, long rowBytes) {
    short x,y;
    for (y=CHAR_HEIGHT;y>0;y--) {
        for (x=CHAR_WIDTH;x>0;x--) {
            if (pixBytes==4) pixMap++;
            *(pixMap++)=0;
            *(pixMap++)=0;
            *(pixMap++)=0;
        }
        pixMap+=rowBytes-CHAR_WIDTH*pixBytes;
    }
}


void MiniDrawChar(unsigned char* pixMap, short pixBytes, long rowBytes, char ascii) {
    short x,y;
    unsigned char* src;
    if (ascii<0) MiniDrawSpace(pixMap,pixBytes,rowBytes);
    else {
        ascii=charLookup[(short)ascii];
        if ((ascii<0)||(ascii>=NUM_CHARS)) MiniDrawSpace(pixMap,pixBytes,rowBytes);
        else {
            src=charPixelData+CHAR_WIDTH*CHAR_HEIGHT*ascii;
            for (y=CHAR_HEIGHT;y>0;y--) {
                for (x=CHAR_WIDTH;x>0;x--) {
                    if (pixBytes==4) pixMap++;
                    *(pixMap++)=*src;
                    *(pixMap++)=*src;
                    *(pixMap++)=*(src++);
                }
                pixMap+=rowBytes-CHAR_WIDTH*pixBytes;
            }
        }
    }
}


void MiniDrawString(unsigned char* pixMap, short pixBytes, long rowBytes, short x, short y,char* str) {
    char c;
    unsigned char* pixMapRun;
    if (!str) return;
    pixMap+=pixBytes*x;
    pixMap+=rowBytes*y;
    pixMapRun=pixMap;
    while ((*str)!=0) {
        c=*(str++);
        if ((c==13)||(c==10)) {
            pixMap+=(CHAR_HEIGHT+3)*rowBytes;
            pixMapRun=pixMap;
        } else {
            MiniDrawChar(pixMapRun,pixBytes,rowBytes,c);
            pixMapRun+=pixBytes*CHAR_WIDTH;
        }
    }
}